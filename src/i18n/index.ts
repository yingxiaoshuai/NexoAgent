const translations = { zh, en } as const;

export type TranslationKey = keyof typeof en;

function formatMessage(template: string, params?: TranslationParams) {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, key) => {
    const value = params[key];
    return value === undefined ? `{${key}}` : String(value);
  });
}

interface I18nContextValue {
  lang: Lang;
  setLang: (lang: Lang) => void;
  t: (key: TranslationKey, params?: TranslationParams) => string;
}

const I18nContext = createContext<I18nContextValue>({
  lang: "zh",
  setLang: () => {},
  t: (key) => key,
});

export const I18nProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [lang, setLangState] = useState<Lang>(() => (localStorage.getItem("nexo-lang") as Lang) || "zh");

  const setLang = (next: Lang) => {
    setLangState(next);
    localStorage.setItem("nexo-lang", next);
  };

  return React.createElement(
    I18nContext.Provider,
    {
      value: {
        lang,
        setLang,
        t: (key, params) => formatMessage(translations[lang][key] || translations.en[key] || key, params),
      },
    },
    children,
  );
};

export const useI18n = () => useContext(I18nContext);
